import UIKit

extension UIColor {
    
    static var primary: UIColor {
        return .red
    }
    
    static var secondary: UIColor {
        return .black
    }
    
    static var tertiary: UIColor {
        return UIColor(red: 235/255.0, green: 235/255.0, blue: 241/255.0, alpha: 1.0)
    }
    
    static var background: UIColor {
        return .white
    }
}

